//Ventana emergente El Jueves

const btnAbrirVentana1 =
document.querySelector("#btn-Abrir-Ventana-1")

const btnCerrarVentana1 = 
document.querySelector("#btn-Cerrar-Ventana-1")

const Ventana_Emergente_1 =
document.querySelector("#Ventana_Emergente_1")

btnAbrirVentana1.addEventListener("click",()=>{
    Ventana_Emergente_1.showModal();
})

btnCerrarVentana1.addEventListener("click",()=>
{
    Ventana_Emergente_1.close();
})

//Boton más información El Jueves

let btn_esconder_texto1 =
document.getElementById('btn_esconder_texto1')

let esconder_texto1 =
document.getElementById('esconder_texto1')

btn_esconder_texto1.addEventListener('click', toggleText1);

function toggleText1(){

esconder_texto1.classList.toggle('ensenyar_text_finestra_emergent');

if(esconder_texto1.classList.contains('ensenyar_text_finestra_emergent')){

btn_esconder_texto1.innerHTML = 'Menos Información';

}
else{

btn_esconder_texto1.innerHTML = 'Más Información'

}

}

